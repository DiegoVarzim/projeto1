# Tipos int e float

# Int -> Número inteiro.
# O tipo int representa qual número positivo ou negativo. Int sem sinal é considerado positivo.

# Exemplos:
print(11) #int
print(-11) #int
print(0)

# cmd + / -> comenta


# Float -> Número com ponto flutuante.
# O tipo float representa qualquer número positivo ou negativo com ponto flutuante. 
#Float sem sinal é considerado positivo.

# Exemplos:
print(1.10, 10.11)
print (0.0, -1.5)

# A função type mostra o tipo que o Python inferiu ao valor.
print(type('Diego'))
print(type(-1))
print(type(1.1), type(-1.1), type(0.0))

