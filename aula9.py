adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicacao', multiplicacao)

divisao = 10 / 2.2 # Float - sempre retornará número flutuante, independente do número ser inteiro ou flutuante.
print('Divisão', divisao)

divisao_inteira = 10 // 2.2 # Trunca o número (arredondamento)
print('Divisão inteira', divisao_inteira)

exponenciacao = 2 ** 10 # Potenciação (tomar cuidado com contas elevadas p/ não estour a memória)
print('Exponenciação', exponenciacao)

modulo = 55 % 2 # Resto da divisão
print('Módulo', modulo)

print(10 % 8 == 0)
print(16 % 8 == 0)
print(10 % 2 == 0)
print(15 % 2 == 0)
print(16 % 2 == 0)















