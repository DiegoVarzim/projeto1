# Conversão de tipos, coerção, type convertion, typecasting, coercion -         é o ato de converter um tipo em outro:

# Tipos imutáveis e primitivos:
# str, int, float, bool

print(int('1'), type(int('1'))) # Conversão de string para inteiro
print(int('1')+ 1) # Soma da conversão
print(float('1')+ 1) # Soma da conversão
print(bool(' ')) # Soma da conversão
print(str(11) + 'b') # Concatenar
print(type(float('1')+ 1)) # Soma da conversão
print(1 + 1)
